#include "../../minishell.h"

int	is_dollar_word(char *line, int i)
{
	if (line == NULL)
		return (0);
	if (line[i] == '$' && line[i + 1] != '\0'
		&& line[i + 1] != ' '
		&& line[i + 1] != '$')
		return (1);
	return (0);
}
